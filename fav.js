    // Sélectionner la div
    const fav1 = document.getElementById('fav1');

    // Ajouter un EventListener pour écouter les clics
    fav1.addEventListener('click', () => {
      console.log('Div cliquée !'); // Log pour indiquer que l'événement a été déclenché

      // Vérifier la classe actuelle
      if (fav1.classList.contains('inactive')) {
        console.log('Classe actuelle : inactive');
        fav1.classList.remove('inactive');
        fav1.classList.add('active');
        console.log('Classe changée en : active');
      } else {
        console.log('Classe actuelle : active');
        fav1.classList.remove('active');
        fav1.classList.add('inactive');
        console.log('Classe changée en : inactive');
      }
    });

